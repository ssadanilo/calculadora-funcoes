def adicao():
    a = int(input('Digite o primeiro número: '))
    b = int(input('Digite o segundo número: '))
    return a + b

def divisao():
    a = int(input('Digite o primeiro número: '))
    b = int(input('Digite o segundo número: '))
    return a / b

def subtracao():
    a = int(input('Digite o primeiro número: '))
    b = int(input('Digite o segundo número: '))
    return a - b

def potencia():
    a = int(input('Digite o primeiro número: '))
    b = int(input('Digite o segundo número: '))
    return a ** b

def multiplicacao():
    a = int(input('Digite o primeiro número: '))
    b = int(input('Digite o segundo número: '))
    return a * b
      